package jpipe;

public class Instruccion {
	private int codOp;
	private int ra;
	private int rb;
	private int rc;
	
	public int getCodOp() {
		return codOp;
	}
	public void setCodOp(int codOp) {
		this.codOp = codOp;
	}
	public int getRa() {
		return ra;
	}
	public void setRa(int ra) {
		this.ra = ra;
	}
	public int getRb() {
		return rb;
	}
	public void setRb(int rb) {
		this.rb = rb;
	}
	public int getRc() {
		return rc;
	}
	public void setRc(int rc) {
		this.rc = rc;
	}
}
